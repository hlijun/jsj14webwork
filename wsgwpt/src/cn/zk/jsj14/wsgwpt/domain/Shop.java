package cn.zk.jsj14.wsgwpt.domain;

public class Shop {
	private String shop_name;
	private String shop_pwd;
	private String shop_username;
	private String shop_usertel;
	private String shop_useraddress;
	public String getShop_name() {
		return shop_name;
	}
	public void setShop_name(String shop_name) {
		this.shop_name = shop_name;
	}
	public String getShop_pwd() {
		return shop_pwd;
	}
	public void setShop_pwd(String shop_pwd) {
		this.shop_pwd = shop_pwd;
	}
	public String getShop_username() {
		return shop_username;
	}
	public void setShop_username(String shop_username) {
		this.shop_username = shop_username;
	}
	public String getShop_usertel() {
		return shop_usertel;
	}
	public void setShop_usertel(String shop_usertel) {
		this.shop_usertel = shop_usertel;
	}
	public String getShop_useraddress() {
		return shop_useraddress;
	}
	public void setShop_useraddress(String shop_useraddress) {
		this.shop_useraddress = shop_useraddress;
	}
}
